    private void jPanel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MousePressed
        if (SwingUtilities.isRightMouseButton(evt)) {
            Color nuevoColor = JColorChooser.showDialog(this, "Selecciona un color", colorActual);
            if (nuevoColor != null) {
                colorActual = nuevoColor;
            }
        } else {
            xInicio = evt.getX();
            yInicio = evt.getY();
        }
    }//GEN-LAST:event_jPanel1MousePressed

    private void jPanel1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseReleased
        xFin = evt.getX();
        yFin = evt.getY();
        lineas.add(new Linea(xInicio, yInicio, xFin, yFin, colorActual));
        dibujarLineasEnPanel();
 
    }//GEN-LAST:event_jPanel1MouseReleased

    private void jPanel1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPanel1KeyPressed
       if (evt.getKeyCode() == KeyEvent.VK_B) {
            modoBorrado = !modoBorrado; 
        }
    }//GEN-LAST:event_jPanel1KeyPressed

    private void jPanel1MouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseDragged
        xFin = evt.getX();
        yFin = evt.getY();

        if (modoBorrado) {
            borrados.add(new PuntoBorrado(evt.getX(), evt.getY(), radioBorrador, jPanel1.getBackground()));
        } else {
            lineas.add(new Linea(xInicio, yInicio, xFin, yFin, colorActual));
        }

        xInicio = xFin;
        yInicio = yFin;
        dibujarLineasEnPanel();
    }//GEN-LAST:event_jPanel1MouseDragged

