    private static class PuntoBorrado {
        int x, y, radio;
        Color color;

        public PuntoBorrado(int x, int y, int radio, Color color) {
            this.x = x;
            this.y = y;
            this.radio = radio;
            this.color = color;
        }
    }
    
    private static class Linea {
        int x1, y1, x2, y2;
        Color color;

        public Linea(int x1, int y1, int x2, int y2, Color color) {
            this.x1 = x1;
            this.y1 = y1;
            this.x2 = x2;
            this.y2 = y2;
            this.color = color;
        }
    }

