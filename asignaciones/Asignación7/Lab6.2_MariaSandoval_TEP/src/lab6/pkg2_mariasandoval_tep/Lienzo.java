    private void dibujarLineasEnPanel() {
        Graphics g = jPanel1.getGraphics();
        g.setColor(jPanel1.getBackground());
        g.fillRect(0, 0, jPanel1.getWidth(), jPanel1.getHeight());
        for (Linea l : lineas) {
            g.setColor(l.color);
            g.drawLine(l.x1, l.y1, l.x2, l.y2);
        }
        for (PuntoBorrado p : borrados) {
            g.setColor(p.color);
            g.fillOval(p.x - p.radio/2, p.y - p.radio/2, p.radio, p.radio);
        }
    }

